void create()
{
	seteuid(getuid());
	if (sizeof(f_name) != sizeof(f_id))
		write("姓氏有错误。\n");
	if (sizeof(name_m) != sizeof(id_m))
		write("男名有错误。\n");
	if (sizeof(name_g) != sizeof(id_g))
		write("女名有错误。\n");
}

